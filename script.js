// DOM 元素声明
let homePage, detailPage, backBtn, detailCityName, detailCityAncient;
let timelineContainer, historyStories, modernInfo, cityCards;

// 页面切换函数
function showPage(pageName) {
    if (pageName === 'home') {
        homePage.classList.add('active');
        detailPage.classList.remove('active');
    } else {
        homePage.classList.remove('active');
        detailPage.classList.add('active');
    }
}

// 渲染城市详情
function renderCityDetail(cityKey) {
    const city = citiesData[cityKey];
    
    // 设置标题
    detailCityName.textContent = city.name;
    detailCityAncient.textContent = `古称：${city.ancientNames}`;
    
    // 渲染时间线
    renderTimeline(city.timeline);
    
    // 渲染历史故事
    renderStories(city.stories);
    
    // 渲染现代发展
    renderModern(city.modern);
}

// 渲染时间线
function renderTimeline(timeline) {
    timelineContainer.innerHTML = timeline.map(item => `
        <div class="timeline-item">
            <div class="timeline-year">${item.year}</div>
            <div class="timeline-dynasty">${item.dynasty}</div>
            <div class="timeline-name">名称：${item.name}</div>
            <div class="timeline-desc">${item.description}</div>
        </div>
    `).join('');
}

// 渲染历史故事
function renderStories(stories) {
    historyStories.innerHTML = stories.map(story => `
        <div class="story-card">
            <h3 class="story-title">${story.title}</h3>
            <div class="story-meta">${story.period}</div>
            <div class="story-content">${story.content}</div>
        </div>
    `).join('');
}

// 渲染现代发展
function renderModern(modern) {
    modernInfo.innerHTML = modern.map(item => `
        <div class="modern-card">
            <h3 class="modern-card-title">${item.title}</h3>
            <div class="modern-card-content">${item.content}</div>
        </div>
    `).join('');
}

// 筛选状态
let currentFilters = {
    region: 'all',
    terrain: 'all',
    historical: 'all'
};

// DOM 元素（筛选相关）
let filterButtons, resultCount, resetFilterBtn;

// 检查城市是否符合筛选条件
function matchFilter(cityKey, filterType, filterValue) {
    if (filterValue === 'all') return true;
    
    const city = citiesData[cityKey];
    if (!city || !city.categories) return false;
    
    const categories = city.categories;
    
    if (filterType === 'region' && categories.region) {
        return categories.region.includes(filterValue);
    }
    if (filterType === 'terrain' && categories.terrain) {
        return categories.terrain.includes(filterValue);
    }
    if (filterType === 'historical' && categories.historical) {
        return categories.historical.includes(filterValue);
    }
    
    return false;
}

// 应用筛选
function applyFilters() {
    let visibleCount = 0;
    
    cityCards.forEach(card => {
        const cityKey = card.getAttribute('data-city');
        let isVisible = true;
        
        // 检查所有筛选条件
        for (const filterType in currentFilters) {
            if (!matchFilter(cityKey, filterType, currentFilters[filterType])) {
                isVisible = false;
                break;
            }
        }
        
        // 显示/隐藏卡片
        if (isVisible) {
            card.style.display = 'block';
            card.style.animation = 'fadeInUp 0.6s ease forwards';
            visibleCount++;
        } else {
            card.style.display = 'none';
        }
    });
    
    // 更新结果计数
    if (resultCount) {
        resultCount.textContent = `共找到 ${visibleCount} 个城市`;
    }
}

// 重置筛选
function resetAllFilters() {
    currentFilters = {
        region: 'all',
        terrain: 'all',
        historical: 'all'
    };
    
    // 重置按钮状态
    const allFilterBtns = document.querySelectorAll('.filter-btn');
    allFilterBtns.forEach(btn => {
        if (btn.getAttribute('data-value') === 'all') {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    applyFilters();
}

// 初始化函数
function init() {
    // 获取DOM元素
    homePage = document.getElementById('home-page');
    detailPage = document.getElementById('detail-page');
    backBtn = document.getElementById('back-btn');
    detailCityName = document.getElementById('detail-city-name');
    detailCityAncient = document.getElementById('detail-city-ancient');
    timelineContainer = document.getElementById('timeline-container');
    historyStories = document.getElementById('history-stories');
    modernInfo = document.getElementById('modern-info');
    cityCards = document.querySelectorAll('.city-card');
    
    // 获取筛选相关DOM元素
    filterButtons = document.querySelectorAll('.filter-btn');
    resultCount = document.getElementById('result-count');
    resetFilterBtn = document.getElementById('reset-filter');

    // 事件监听 - 城市卡片点击
    cityCards.forEach(card => {
        card.addEventListener('click', () => {
            const cityKey = card.getAttribute('data-city');
            renderCityDetail(cityKey);
            showPage('detail');
            window.scrollTo(0, 0);
        });
    });

    // 事件监听 - 返回按钮
    backBtn.addEventListener('click', () => {
        showPage('home');
        window.scrollTo(0, 0);
    });
    
    // 事件监听 - 筛选按钮
    if (filterButtons) {
        filterButtons.forEach(btn => {
            btn.addEventListener('click', () => {
                const filterType = btn.parentElement.getAttribute('data-filter-type');
                const filterValue = btn.getAttribute('data-value');
                
                // 更新同组按钮状态
                const siblingBtns = btn.parentElement.querySelectorAll('.filter-btn');
                siblingBtns.forEach(sb => sb.classList.remove('active'));
                btn.classList.add('active');
                
                // 更新筛选状态并应用
                currentFilters[filterType] = filterValue;
                applyFilters();
            });
        });
    }
    
    // 事件监听 - 重置筛选按钮
    if (resetFilterBtn) {
        resetFilterBtn.addEventListener('click', resetAllFilters);
    }
    
    // 初始化筛选
    applyFilters();

    // 显示首页
    showPage('home');
}

// 页面加载完成后初始化
document.addEventListener('DOMContentLoaded', init);

// 如果DOM已经加载完成，立即初始化
if (document.readyState === 'complete' || document.readyState === 'interactive') {
    init();
}
